package com.github.meeplemeet.ui.theme

import androidx.compose.ui.unit.dp

/** Standardized elevation levels for UI components, to match with Figma */
object Elevation {
  val none = 0.dp // Flat
  val subtle = 1.dp // Slight Hover
  val raised = 3.dp // Small float
  val floating = 6.dp // Medium float
  val lifted = 8.dp // Prominent float
  val dialog = 15.dp // Dialog boxes
}
